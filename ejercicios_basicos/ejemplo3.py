# para especificar el tipo a string
x = str(54)
print("x: ",x)

# para especificar el tipo a number del tipo int
y = int(3.56)
print("y: ",y)
z = int("3")
print("z: ",z)

# para especificar el tipo a number del tipo floar
r = float(567)
print("r: ",r)
w = float("3.56")
print("w: ",w)

# para obtener el tipo
print("x: ", type(x))
print("y: ", type(y))
print("z: ", type(z))
print("w: ", type(w))
print("r: ", type(r))